    private String createNewBloodCollectionRequest() {
        
        DateFormat dateFormat = new SimpleDateFormat("YYYY-MM-dd");
        
        return new StringBuilder()
                .append("Insert into BloodCollectionRequests values (")
                .append(Integer.valueOf(animalID.getText()))
                .append(", '")
                .append(animalType.getText())
                .append("', '")
                .append(bloodType.getText()).append("', ")
                .append(Integer.valueOf(units.getText()))
                .append(",'")
                .append(dateFormat.format(requiredDate.getDate()))
                .append("', ")
                .append("7").append(",'").append("SupportPersonName")
                .append("', '")
                .append(WorkFlowStatus.PENDING)
                .append("')")
                .toString();
    }

